package beans;

import org.springframework.stereotype.Component;
import org.springframework.stereotype.Service;

@Service
public class CatOne {

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Cat{" +
                "name='" + name + '\'' +
                '}';
    }
}
